export function getEncyclopediaEntry(cardId) {
  if (ENCYCLOPEDIA[cardId]) {
    return ENCYCLOPEDIA[cardId]
  }
  return null
}

export function hasEncyclopediaEntry(cardId) {
  return !!getEncyclopediaEntry(cardId)
}

export function getAllEncyclopediaIds() {
  return Object.keys(ENCYCLOPEDIA)
}

export function getRelatedCards(cardId, relationType = null) {
  const entry = getEncyclopediaEntry(cardId)
  if (!entry || !entry.keywordRelations) return []
  if (!relationType) {
    return [
      ...entry.keywordRelations.synergistic,
      ...entry.keywordRelations.conflicting,
      ...entry.keywordRelations.transformational
    ]
  }
  return entry.keywordRelations[relationType] || []
}
